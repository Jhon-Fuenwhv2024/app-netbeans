    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (txtFuncionarioId.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccionar funcionario de la lista");
            txtFuncionarioId.requestFocus();
            return;
        }

        if (txtNameEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite la Nombre");
            txtNameEdit.requestFocus();
            return;
        }

        if (txtN_IdentificacionEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el NÂ° Identificacion");
            txtN_IdentificacionEdit.requestFocus();
            return;
        }

        if (txtSexEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el Sexo");
            txtSexEdit.requestFocus();
            return;
        }

        if (txtDireccioEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite la direccion");
            txtDireccioEdit.requestFocus();
            return;
        }

        if (txtPhoneEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el telefono");
            txtPhoneEdit.requestFocus();
            return;
        }

        Funcionario funcionario = new Funcionario();
        funcionario.setNombres(txtNameEdit.getText().trim());
        funcionario.setNumero_identificacion(txtN_IdentificacionEdit.getText().trim());
        funcionario.setSexo(txtSexEdit.getText().trim());
        funcionario.setDireccion(txtDireccioEdit.getText().trim());
        funcionario.setTelefono(txtPhoneEdit.getText().trim());

        int opt = JOptionPane.showConfirmDialog(null, "Desea actualizar el funcionario", "Confirmar salida",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (opt == 0) {
            try {
                funcionarioController.actualizarFuncionario(Integer.parseInt(txtFuncionarioId.getText()), funcionario);
                txtFuncionarioId.setText("");
                txtNameEdit.setText("");
                txtN_IdentificacionEdit.setText("");
                txtSexEdit.setText("");
                txtDireccioEdit.setText("");
                txtPhoneEdit.setText("");
                listFuncionarios();
                JOptionPane.showMessageDialog(null, "Se ha actualizado creado con Ã©xito");

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "No se pudo actualizar el funcionario");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

