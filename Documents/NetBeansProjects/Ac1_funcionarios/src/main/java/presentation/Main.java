    private void listFuncionarios() {
        cbxFuncionarios.removeAllItems();
        Funcionario funcionario1 = new Funcionario();
        funcionario1.setNombres(SELECCIONE);
        funcionario1.setSexo("");
        cbxFuncionarios.addItem(funcionario1);
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS){
            defaultTableModel.addColumn(COLUMN);
        }
        
        tblFuncionarios.setModel(defaultTableModel);
        
        try {
            
            List<Funcionario> funcionarios = funcionarioController.obtenerFuncionarios();
            if (funcionarios.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(funcionarios.size());
            int row = 0;
            for (Funcionario funcionario : funcionarios){
                defaultTableModel.setValueAt(funcionario.getFuncionarios_id(), row, 0);
                defaultTableModel.setValueAt(funcionario.getNumero_identificacion(), row, 1);
                defaultTableModel.setValueAt(funcionario.getNombres(), row, 2);
                defaultTableModel.setValueAt(funcionario.getSexo(), row, 3);
                defaultTableModel.setValueAt(funcionario.getDireccion(), row, 4);
                defaultTableModel.setValueAt(funcionario.getTelefono(), row, 5);
                row++;
                
                cbxFuncionarios.addItem(funcionario);
                
            }
        } catch (SQLException ex) {
            
            ex.printStackTrace();
        }
    }
    
    private void addListener() {
        cbxFuncionarios.addItemListener(event -> {
        Funcionario selectedFuncionario = (Funcionario) event.getItem();
        if (selectedFuncionario.getNombres().equals(SELECCIONE)) {
            txtFuncionarioId.setText("");
            txtNameEdit.setText("");
            txtN_IdentificacionEdit.setText("");
            txtSexEdit.setText("");
            txtDireccioEdit.setText("");
            txtPhoneEdit.setText("");
        } else {
            txtFuncionarioId.setText(String.valueOf(selectedFuncionario.getFuncionarios_id()));
            txtNameEdit.setText(selectedFuncionario.getNombres());
            txtN_IdentificacionEdit.setText(selectedFuncionario.getNumero_identificacion());
            txtSexEdit.setText(selectedFuncionario.getSexo());
            txtDireccioEdit.setText(selectedFuncionario.getDireccion());
            txtPhoneEdit.setText(selectedFuncionario.getTelefono());
        }
        });
    }

